package org.epoch.core.constants;

/**
 * 符号常量
 */
public interface StringPool {
    /**
     * 感叹号：!
     */
    String SIGH = "!";
    /**
     * 符号：@
     */
    String AT = "@";
    /**
     * 井号：#
     */
    String WELL = "#";
    /**
     * 美元符：$
     */
    String DOLLAR = "$";
    /**
     * 人民币符号：￥
     */
    String RMB = "￥";
    /**
     * 空格：
     */
    String SPACE = " ";
    /**
     * 换行符：\r\n
     */
    String LB = System.getProperty("line.separator");
    /**
     * 百分号：%
     */
    String PERCENTAGE = "%";
    /**
     * 符号：&amp;
     */
    String AND = "&";
    /**
     * 星号
     */
    String STAR = "*";
    /**
     * 中横线：-
     */
    String MIDDLE_LINE = "-";
    /**
     * 下划线：_
     */
    String LOWER_LINE = "_";
    /**
     * 等号：=
     */
    String EQUAL = "=";
    /**
     * 加号：+
     */
    String PLUS = "+";
    /**
     * 冒号：:
     */
    String COLON = ":";
    /**
     * 分号：;
     */
    String SEMICOLON = ";";
    /**
     * 逗号：,
     */
    String COMMA = ",";
    /**
     * 点号：.
     */
    String POINT = ".";
    /**
     * 斜杠：/
     */
    String SLASH = "/";
    /**
     * 竖杠：|
     */
    String VERTICAL_BAR = "|";
    /**
     * 双斜杠：//
     */
    String DOUBLE_SLASH = "//";
    /**
     * 反斜杠
     */
    String BACKSLASH = "\\";
    /**
     * 问号：?
     */
    String QUESTION = "?";
    /**
     * 左花括号：{
     */
    String LEFT_BIG_BRACE = "{";
    /**
     * 右花括号：}
     */
    String RIGHT_BIG_BRACE = "}";
    /**
     * 左中括号：[
     */
    String LEFT_MIDDLE_BRACE = "[";
    /**
     * 右中括号：[
     */
    String RIGHT_MIDDLE_BRACE = "]";
    /**
     * 反引号：`
     */
    String BACKQUOTE = "`";
}
